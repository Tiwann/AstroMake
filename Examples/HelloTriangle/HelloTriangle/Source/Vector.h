#pragma once

struct vec2
{
    float x, y;

    vec2(float x, float y);
    vec2();
    vec2(float n);
};

struct vec3
{
    float x, y, z;

    vec3(float x, float y, float z);
    vec3();
    vec3(float n);
};

struct vec4
{
    float x, y, z, w;

    vec4(float x, float y, float z, float w);
    vec4();
    vec4(float n);
};